; ====================================================================
; -------------------------------------------------
; Z80 Side
; -------------------------------------------------

; ====================================================================
; -------------------------------------------------
; Init
; -------------------------------------------------

		di
		ld	sp,01FFFh
		ld	de,0
		ld	a,2Bh
		ld	(4000h),a
		ld	a,00h
		ld	(4001h),a
		
; ====================================================================
; -------------------------------------------------
; Loop
; -------------------------------------------------

Loop:
		ld	a,(driver_ctrl)
		bit 	7,a			; Playing mode: $80?
		jp	nz,@playing		; Yes
		bit 	6,a			; Pause request: $40?
		jp	nz,@pause		; Yes
		bit 	5,a			; Resume request: $20?
		jp	nz,@resume		; Yes
		bit 	0,a			; New sample request? ($01 or $03)
		jp 	z,Loop			; Nope
		
; --------------------------------------
; Set new sample
; --------------------------------------	

		ld	a,(chn_addr_start)
		ld 	e,a
		ld 	a,(chn_bank_start+1)
		ld 	d,a
		call	BankSwitch
		ld	a,(chn_addr_start)
		ld 	h,a
		ld	a,(chn_addr_start+1)
		ld	l,a
		
		ld 	a,(driver_ctrl)
		res 	0,a
		ld 	(driver_ctrl),a
		
@restart:
		push	de
		exx
		pop 	de
		
		call	End_Check
		exx

		set 	7,h
		ld 	a,(driver_ctrl)
		or	80h
		ld 	(driver_ctrl),a
		ld	de,0
		ld	a,2Bh
		ld	(4000h),a
		ld	a,80h
		ld	(4001h),a
		
; --------------------------------------
; Loop
; --------------------------------------

@playing:
 		ld	a,2Ah
		ld	(4000h),a
  		ld	a,(hl)
		ld	(4001h),a
		ld	(driver_lastsmp),a
		
		ld	a,(driver_flags)
		cp	3
		jp	nz,@Continue_Slow
		
; --------------------------------------
; EOF check
; --------------------------------------

 		ld	a,(chn_addr_end)
 		or	80h
 		cp	h
 		jp	nz,@Continue
 		ld	a,(chn_addr_end+1)
 		cp	l
 		jp	z,@TheSame 
 		jp	nc,@Continue
@TheSame:
    		ld	a,(driver_ctrl)
  		bit 	1,a
 		jp 	z,@dont_loop
		
		ld	a,(chn_addr_loop)
		ld 	e,a
		ld 	a,(chn_bank_loop+1)
		ld 	d,a
		call	BankSwitch
		ld	a,(chn_addr_loop)
		ld 	h,a
		ld	a,(chn_addr_loop+1)
		ld	l,a
		
		xor	a
		ld	(driver_flags),a
 		jp	@restart
 	
; --------------------------------------
; Pause / Stop
; --------------------------------------

@pause:
   		res 	6,a
@dont_loop:
   		res 	7,a
  		ld	(driver_ctrl),a
  		
		ld	a,2Bh
		ld	(4000h),a
		ld	a,00h
		ld	(4001h),a
 
		jp	Loop
	
; --------------------------------------
; Resume
; --------------------------------------

@resume:
   		set	7,a
  		ld	(driver_ctrl),a
  		
		ld	a,2Bh
		ld	(4000h),a
		ld	a,80h
		ld	(4001h),a
 
		jp	Loop
		
; --------------------------------------
; Continue playing
; --------------------------------------

@Continue_Slow:	
 		ld	de,0
 		ld	de,0
 		ld	de,0
 		nop
@Continue:
		ld	a,c
		ld	bc,chn_pitchlist		;<--- $A000D6
		inc	a
		and	3Fh
		or	c
		ld	c,a
		ld	a,(bc)
		ld	e,a
		add	hl,de
		
; 		nop 
; 		nop 
; 		nop
 		
		bit 	7,h
		jp	nz,Loop
		
; --------------------------------------
; Next bank
; --------------------------------------x

		set 	7,h
		exx
		ld 	a,e
		cpl
		ld 	e,a
		bit 	7,a
		jp	nz,@half
		inc 	d
@half:
		call	BankSwitch
		
		call	End_Check
		exx
		jp	Loop
		
; ====================================================================
; -------------------------------------------------
; Subs
; -------------------------------------------------

; --------------------------------------
; BankSwitch
; 
; Input:
; d - XX00000
; e - 0000 | 8000
; --------------------------------------

BankSwitch:
		ld 	a,e
		rlca
		ld 	(6000h),a
		ld	a,d
		ld 	(6000h),a
		rrca
		ld 	(6000h),a
		rrca
		ld 	(6000h),a
		rrca
		ld 	(6000h),a
		rrca
		ld 	(6000h),a
		rrca
		ld 	(6000h),a
		rrca
		ld 	(6000h),a
		rrca
		ld 	(6000h),a
		
		ret
	
; --------------------------------------
; End_Check
; 
; USE EXX FIRST
; --------------------------------------

End_Check:
		ld	a,e
		and 	80h
		ld	c,a
		ld	a,(driver_flags)
		ld 	b,a
		ld 	a,(chn_addr_end)
		and 	80h
		res 	0,b
		res 	1,b
		cp 	c
		jp 	nz,@same_half
		set	0,b
@same_half:		
		ld 	a,(chn_bank_end+1)
		cp 	d
		jp 	nz,@same_lsb
		set	1,b
@same_lsb:
		ld 	a,b
		ld 	(driver_flags),a
		ret

